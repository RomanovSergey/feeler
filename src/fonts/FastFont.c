const char* getImg5x8( const uint16_t code ) {
	int i;

	if ( code < 0x20 ) {
		return fast_0x3F;// '?'
	} else if ( code <= 0x7E ) {
		i = code - 0x20;
		return FastArr1[i];
	} else if ( code <= 0xD0BF ) {
		if ( code >= 0xD090 ) {
			i = code - 0xD090;
			return FastArr2[i];
		} else {
			if ( code == 0x85 ) {
				return fast_0x85;// '...'
			} else if (code == 0xD081) {
				return fast_0xD081;// Ё
			}
		}
	} else if ( code <= 0xD18F ) {
		if ( code >= 0xD180 ) {
			i = code - 0xD180;
			return FastArr3[i];
		}
	} else if ( code == 0xD191 ) {
		return fast_0xD191;// ё
	}
	return fast_0x3F;// '?'
}

