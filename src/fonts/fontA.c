//через алгоритм бинарного поиска
const char* getFont5x8( const uint16_t key) {
	int len = sizeof( fontA ) / sizeof ( TUcode_t );

	int found = 0;
	int high = len - 1, low = 0;
	int middle = (high + low) / 2;
	while ( !found && high >= low ){
		if ( key == fontA[middle].code ) {
			found = 1;
			break;
		} else if (key < fontA[middle].code ) {
			high = middle - 1;
		} else {
			low = middle + 1;
		}
		middle = (high + low) / 2;
	}
	return (found == 1) ? fontA[middle].img : f_0x3F ;
}

